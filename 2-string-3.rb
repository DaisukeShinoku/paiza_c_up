# 0 ~ 9 の数字が 4 つ並んだ文字列 S が与えられます。
# 左から 1 番目の数と 4 番目の数を足し合わせたものを a とし、
# 2 番目の数と 3 番目の数を足し合わせたものを b とします。
# 文字列としての a の末尾に文字列としての b を結合したものを出力してください。

s = gets.chomp

a = s.chars

b = a[0].to_i

c = a[1].to_i

d = a[2].to_i

e = a[3].to_i

f = b + e

g = c + d

h = f.to_s

i = g.to_s

j = h + i

puts j